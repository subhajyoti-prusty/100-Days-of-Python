# Round Function:- its a inbuild function to round a number insteard of changing its datatype 
# Example:
x = 12.4567890
y = round(x)
print("The rounded value is :", y)

# In this you can also round it to some specific decimal placeses 
z = round(3.14159, 2)
print("The rounded value with two decimal places is : ", z)
z = round(3.14753,3)
print("The rounded value with three decimal places is : ", z)